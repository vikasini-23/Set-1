// Program: Student Record System - Identify Datatypes

// Declare variables of different types
let studentAge = 20;          // Number
let studentName = "Vikasini";    // String
let isEnrolled = true;        // Boolean

// Print their datatypes using typeof
console.log("Datatype of studentAge: " + typeof studentAge);
console.log("Datatype of studentName: " + typeof studentName);
console.log("Datatype of isEnrolled: " + typeof isEnrolled);

// Expected Output:
// Datatype of studentAge: number
// Datatype of studentName: string
// Datatype of isEnrolled: boolean
