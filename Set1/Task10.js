// Program: Student Record System - Create and Print an Object

// Create a student object with 5 properties
let student = {
    name: "Vikasini",
    age: 20,
    marks: 88,
    course: "Computer Science",
    isEnrolled: true
};

// Print each property individually
console.log("Name: " + student.name);
console.log("Age: " + student.age);
console.log("Marks: " + student.marks);
console.log("Course: " + student.course);
console.log("Enrolled: " + student.isEnrolled);

// Expected Output:
// Name: Vikasini
// Age: 20
// Marks: 88
// Course: Computer Science
// Enrolled: true
